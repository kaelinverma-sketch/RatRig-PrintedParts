def make_sketch_cut_tool(pts, x_pos, y_offset, z_offset, extrude_dist, taper_angle):
    cy = sum(y + y_offset for y, z in pts) / len(pts)
    cz = sum(z + z_offset for y, z in pts) / len(pts)
    taper_shrink = extrude_dist * math.tan(math.radians(taper_angle))
    radii = [math.sqrt((y+y_offset-cy)**2 + (z+z_offset-cz)**2) for y, z in pts]
    avg_r = sum(radii) / len(radii)
    scale = (avg_r + taper_shrink) / avg_r

    base_poly = BRepBuilderAPI_MakePolygon()
    for y, z in pts:
        base_poly.Add(gp_Pnt(x_pos, y + y_offset, z + z_offset))
    base_poly.Close()

    far_poly = BRepBuilderAPI_MakePolygon()
    for y, z in pts:
        ny = cy + ((y+y_offset) - cy) * scale
        nz = cz + ((z+z_offset) - cz) * scale
        far_poly.Add(gp_Pnt(x_pos + extrude_dist, ny, nz))
    far_poly.Close()

    loft = BRepOffsetAPI_ThruSections(True, True)
    loft.AddWire(base_poly.Wire())
    loft.AddWire(far_poly.Wire())
    loft.Build()
    if not loft.IsDone():
        raise RuntimeError("Sketch loft failed.")
    return loft.Shape()

