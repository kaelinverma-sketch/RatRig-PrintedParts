# Both profiles have 3 structural sections:
#   Arc  : P1[0:14] (14 pts), P2[0:13] (13 pts)
#   Rect : P1[14:17] (3 pts),  P2[13:16] (3 pts) — exact match
#   Inner: P1[17:25] (8 pts),  P2[16:25] (9 pts)
# Resample each section to equal count then concatenate → no twist

N_ARC   = 50
N_RECT  = 3   # keep exact corners
N_INNER = 30

def resample_section(pts, n):
    if n <= 1:
        return [pts[0]]
    segs = [math.sqrt((pts[i+1][0]-pts[i][0])**2+(pts[i+1][1]-pts[i][1])**2+(pts[i+1][2]-pts[i][2])**2)
            for i in range(len(pts)-1)]
    total = sum(segs)
    if total < 1e-9:
        return [pts[0]]*n
    cumul = [0.0]
    for s in segs: cumul.append(cumul[-1]+s)
    out = []
    for j in range(n):
        t = total * j / (n-1)
        for k in range(len(cumul)-1):
            if cumul[k] <= t <= cumul[k+1]:
                seg_len = cumul[k+1]-cumul[k]
                frac = (t-cumul[k])/seg_len if seg_len>1e-12 else 0.0
                out.append((
                    pts[k][0]+frac*(pts[k+1][0]-pts[k][0]),
                    pts[k][1]+frac*(pts[k+1][1]-pts[k][1]),
                    pts[k][2]+frac*(pts[k+1][2]-pts[k][2]),
                ))
                break
    return out

