def make_wire(pts_3d):
    poly = BRepBuilderAPI_MakePolygon()
    for x, y, z in pts_3d:
        poly.Add(gp_Pnt(x, y, z))
    poly.Close()
    return poly.Wire()

def face_z_mid(f):
    bbox = Bnd_Box()
    BRepBndLib.Add_s(f, bbox)
    xmin, ymin, zmin, xmax, ymax, zmax = bbox.Get()
    return (zmin + zmax) / 2.0

def horizontal_faces_at_z(shape, z_level, tol=1e-3):
    from OCP.BRepAdaptor import BRepAdaptor_Surface
    from OCP.GeomAbs import GeomAbs_Plane
    result = []
    exp = TopExp_Explorer(shape, TopAbs_FACE)
    while exp.More():
        f = TopoDS.Face_s(exp.Current())
        adaptor = BRepAdaptor_Surface(f)
        if adaptor.GetType() == GeomAbs_Plane:
            normal = adaptor.Plane().Axis().Direction()
            if abs(abs(normal.Z()) - 1.0) < 0.01:
                loc = adaptor.Plane().Location()
                if abs(loc.Z() - z_level) < tol:
                    result.append(f)
        exp.Next()
    return result

def edges_of_faces(faces):
    pairs = []
    for f in faces:
        exp = TopExp_Explorer(f, TopAbs_EDGE)
        while exp.More():
            pairs.append((TopoDS.Edge_s(exp.Current()), f))
            exp.Next()
    return pairs

def safe_cut(base, tool, label):
    op = BRepAlgoAPI_Cut(base, tool)
    op.Build()
    if not op.IsDone():
        raise RuntimeError(f"{label} cut failed.")
    print(f"{label} cut applied.")
    return op.Shape()

def safe_fuse(base, tool, label):
    op = BRepAlgoAPI_Fuse(base, tool)
    op.Build()
    if not op.IsDone():
        raise RuntimeError(f"{label} fuse failed.")
    print(f"{label} fuse applied.")
    return op.Shape()

def arc_length_resample_3d(pts, n):
    segs = [math.sqrt((pts[i+1][0]-pts[i][0])**2 +
                      (pts[i+1][1]-pts[i][1])**2 +
                      (pts[i+1][2]-pts[i][2])**2)
            for i in range(len(pts)-1)]
    total = sum(segs)
    cumul = [0.0]
    for s in segs:
        cumul.append(cumul[-1] + s)
    targets = [total * i / (n - 1) for i in range(n)]
    out = []
    for t in targets:
        for i in range(len(cumul)-1):
            if cumul[i] <= t <= cumul[i+1]:
                seg_len = cumul[i+1] - cumul[i]
                frac = (t - cumul[i]) / seg_len if seg_len > 1e-12 else 0.0
                out.append((
                    pts[i][0] + frac*(pts[i+1][0]-pts[i][0]),
                    pts[i][1] + frac*(pts[i+1][1]-pts[i][1]),
                    pts[i][2] + frac*(pts[i+1][2]-pts[i][2]),
                ))
                break
    return out

def first_corner_index(pts, threshold=100.0):
    for i in range(1, len(pts)-1):
        ax=pts[i][0]-pts[i-1][0]; az=pts[i][2]-pts[i-1][2]
        bx=pts[i+1][0]-pts[i][0]; bz=pts[i+1][2]-pts[i][2]
        la=math.sqrt(ax**2+az**2); lb=math.sqrt(bx**2+bz**2)
        if la<1e-9 or lb<1e-9: continue
        cos_a=max(-1.,min(1.,(ax*bx+az*bz)/(la*lb)))
        if math.degrees(math.acos(cos_a)) > threshold:
            return i
    return 0

def translate(shape, dx=0, dy=0, dz=0):
    t = gp_Trsf()
    t.SetTranslation(gp_Vec(dx, dy, dz))
    return BRepBuilderAPI_Transform(shape, t, True).Shape()

def closest_start_index(ref_pt, pts):
    """Return index in pts whose XZ is closest to ref_pt XZ."""
    rx, rz = ref_pt[0], ref_pt[2]
    best_i, best_d = 0, float('inf')
    for i, (x, y, z) in enumerate(pts):
        d = (x - rx)**2 + (z - rz)**2
        if d < best_d:
            best_d, best_i = d, i
    return best_i

def make_planar_wire_on_y(pts_3d, y_val):
    """Build a closed wire guaranteed to lie on the plane Y=y_val."""
    from OCP.gp import gp_Dir, gp_Pln
    from OCP.TopAbs import TopAbs_WIRE
    plane = gp_Pln(gp_Ax3(gp_Pnt(0.0, y_val, 0.0), gp_Dir(0.0, 1.0, 0.0)))
    poly  = BRepBuilderAPI_MakePolygon()
    for x, y, z in pts_3d:
        poly.Add(gp_Pnt(x, y_val, z))
    poly.Close()
    fm = BRepBuilderAPI_MakeFace(plane, poly.Wire(), True)
    if not fm.IsDone():
        raise RuntimeError(f"Planar face on Y={y_val} failed: {fm.Error()}")
    exp = TopExp_Explorer(fm.Face(), TopAbs_WIRE)
    return TopoDS.Wire_s(exp.Current())

