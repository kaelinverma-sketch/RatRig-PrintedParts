def edge_z_mid(edge):
    props = GProp_GProps()
    BRepGProp.LinearProperties_s(edge, props)
    return props.CentreOfMass().Z()

all_faces = []
fexp = TopExp_Explorer(solid, TopAbs_FACE)
while fexp.More():
    all_faces.append(TopoDS.Face_s(fexp.Current()))
    fexp.Next()

def lateral_face_for(edge):
    adjacent = []
    for f in all_faces:
        eexp = TopExp_Explorer(f, TopAbs_EDGE)
        while eexp.More():
            if eexp.Current().IsSame(edge):
                adjacent.append(f)
                break
            eexp.Next()
    for f in adjacent:
        surf = BRepAdaptor_Surface(f)
        if surf.GetType() == GeomAbs_Plane:
            if abs(surf.Plane().Axis().Direction().Z()) < 0.1:
                return f
    return adjacent[0] if adjacent else None

TOP_Z, BOT_Z, TOL = 0.0, -150.0, 1.0
top_edges, bot_edges = [], []
eexp = TopExp_Explorer(solid, TopAbs_EDGE)
while eexp.More():
    e = TopoDS.Edge_s(eexp.Current())
    z = edge_z_mid(e)
    if   abs(z - TOP_Z) < TOL: top_edges.append(e)
    elif abs(z - BOT_Z) < TOL: bot_edges.append(e)
    eexp.Next()
print(f"Top edges (Z≈0): {len(top_edges)}  Bottom edges (Z≈-150): {len(bot_edges)}")

chamfer = BRepFilletAPI_MakeChamfer(solid)
for e in top_edges + bot_edges:
    f = lateral_face_for(e)
    if f:
        chamfer.Add(6.0, 3.0, e, f)
chamfer.Build()
if chamfer.IsDone():
    result = chamfer.Shape()
