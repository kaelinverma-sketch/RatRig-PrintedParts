import math as _math

def arc_length_resample(pts, n):
    """Resample pts to n evenly-spaced points by 3D arc length."""
    cum = [0.0]
    for i in range(1, len(pts)):
        d = _math.sqrt(sum((pts[i][k]-pts[i-1][k])**2 for k in range(3)))
        cum.append(cum[-1] + d)
    total = cum[-1]
    result = []
    for j in range(n):
        t = j * total / (n - 1)
        for k in range(len(cum)-1):
            if cum[k] <= t <= cum[k+1]:
                seg = cum[k+1] - cum[k]
                f = (t - cum[k]) / seg if seg > 0 else 0
                pt = tuple(pts[k][d] + f*(pts[k+1][d]-pts[k][d]) for d in range(3))
                result.append(pt)
                break
    return result

