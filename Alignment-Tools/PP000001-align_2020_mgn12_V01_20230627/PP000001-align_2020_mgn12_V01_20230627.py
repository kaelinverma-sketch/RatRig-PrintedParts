import math as _m11
def _b11_slen(pts, i0, i1):
    return sum(_m11.sqrt(sum((pts[j][k]-pts[j-1][k])**2 for k in range(3)))
               for j in range(i0+1, min(i1+1, len(pts))))

def _b11_seg(pts, i0, i1, n):
    seg=pts[i0:i1+1]
    if len(seg)<2: return [seg[0]]*n
    cum=[0.0]
    for i in range(1,len(seg)):
        d=_m11.sqrt(sum((seg[i][k]-seg[i-1][k])**2 for k in range(3)))
        cum.append(cum[-1]+d)
    total=cum[-1]
    if total==0: return [seg[0]]*n
    result=[]
    for j in range(n):
        t=j*total/(n-1)
        for k in range(len(cum)-1):
            if cum[k]<=t<=cum[k+1]:
                s=cum[k+1]-cum[k]; f=(t-cum[k])/s if s>0 else 0
                result.append(tuple(seg[k][d]+f*(seg[k+1][d]-seg[k][d]) for d in range(3)))
                break
    while len(result)<n: result.append(seg[-1])
    return result

