CHAMFER_VERT   = 3.0
TOL            = 0.01

def make_closed_wire(pts):
    wb = BRepBuilderAPI_MakeWire()
    n = len(pts)
    for i in range(n):
        x0, y0 = pts[i]
        x1, y1 = pts[(i + 1) % n]
        edge = BRepBuilderAPI_MakeEdge(gp_Pnt(x0, y0, 0.0),
                                       gp_Pnt(x1, y1, 0.0)).Edge()
        wb.Add(edge)
    assert wb.IsDone(), "Wire failed"
    return wb.Wire()

