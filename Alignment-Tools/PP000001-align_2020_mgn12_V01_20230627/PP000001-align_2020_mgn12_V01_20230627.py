def make_3d_wire(pts):
    """Build a closed 3D wire from XYZ points using BRepBuilderAPI_MakePolygon."""
    poly = BRepBuilderAPI_MakePolygon()
    for x, y, z in pts:
        poly.Add(gp_Pnt(x, y, z))
    poly.Close()
    assert poly.IsDone(), "Polygon wire failed"
    return poly.Wire()

