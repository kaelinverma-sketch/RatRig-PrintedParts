def _b8_find_corners(pts, thr=60):
    c=[]
    for i in range(1,len(pts)-1):
        p0,p1,p2=pts[i-1],pts[i],pts[i+1]
        v1=(p1[0]-p0[0],p1[2]-p0[2]); v2=(p2[0]-p1[0],p2[2]-p1[2])
        l1=_mm.sqrt(v1[0]**2+v1[1]**2); l2=_mm.sqrt(v2[0]**2+v2[1]**2)
        if l1>0 and l2>0:
            cc=max(-1,min(1,(v1[0]*v2[0]+v1[1]*v2[1])/(l1*l2)))
            if _mm.degrees(_mm.acos(cc))>thr: c.append(i)
    return c

def _b8_slen(pts, i0, i1):
    return sum(_mm.sqrt(sum((pts[j][k]-pts[j-1][k])**2 for k in range(3)))
               for j in range(i0+1, min(i1+1,len(pts))))

def _b8_seg_resample(pts, i0, i1, n):
    seg=pts[i0:i1+1]
    if len(seg)<2: return [seg[0]]*n
    cum=[0.0]
    for i in range(1,len(seg)):
        d=_mm.sqrt(sum((seg[i][k]-seg[i-1][k])**2 for k in range(3)))
        cum.append(cum[-1]+d)
    total=cum[-1]
    if total==0: return [seg[0]]*n
    result=[]
    for j in range(n):
        t=j*total/(n-1)
        for k in range(len(cum)-1):
            if cum[k]<=t<=cum[k+1]:
                s=cum[k+1]-cum[k]; f=(t-cum[k])/s if s>0 else 0
                result.append(tuple(seg[k][d]+f*(seg[k+1][d]-seg[k][d]) for d in range(3)))
                break
    while len(result)<n: result.append(seg[-1])
    return result

